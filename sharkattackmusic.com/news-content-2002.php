<td align="left" valign="top"><font>7.03 - Ok, just to clear things up a bit - I am a wannabe techno geek. <a href="http://news.com.com/2100-1023-941392.html" target="_top">This</a> appears to be the most recent move in the chess (or maybe checkers is more accurate) game between larger labels and the internet music community. It occurred to me that perhaps (warning: I am an optimistic idealist) with consumers now having more opportunities to form their own opinion easily without having to invest much time, money or energy, what really is raising concerns here is that folks just won't be swayed as much by marketing hype as they used to. How easy is it to just listen to a band that you've heard good things about? Is it good? If so, here's my money. Otherwise, SUGGIT. Maybe it will make folks just a bit harder to trick? Of course, that assumes that folks <i>want</i> to form their own opinion on music.<br><br></td>
